async function main() {
  console.log(`Добавяне на описания за ${descriptions.length} подкатегории...\n`)
  let updated = 0

  for (const item of descriptions) {
    await p.subcategory.update({
      where: { id: item.id },
      data: { description: item.description },
    })
    console.log(`✅ id:${item.id} — обновено`)
    updated++
  }

  console.log(`\n🎉 Готово! Обновени: ${updated}`)
}

